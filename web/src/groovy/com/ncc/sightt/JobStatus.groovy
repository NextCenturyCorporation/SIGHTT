package com.ncc.sightt

/**
 * enumeration of possible status conditions for a Job
 */
public enum JobStatus {
    CREATED("The job has been created but tasks do  not exist yet"),
    INITIALIZED("Tasks for the job have all been created"),
    STARTED("Tasks are being sent to the run queue"),
    RUNNING("All tasks for the job have been sent to the run queue"),
    COMPLETE("The job completed successfully"),
    CANCELLED("The user cancelled the job"),
    FAILED("An error caused the job to fail"),
    UNKNOWN("We don't know what's going on...")

    final desc

    JobStatus(String description) {
        this.desc = description
    }
}
